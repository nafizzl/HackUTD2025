<script>
import axios from 'axios';

export default {
  name: 'HelloWorld',
  data() {
    return {
      message: ''
    };
  },
  mounted() {
    axios.get('http://127.0.0.1:5000/api/hello')
      .then(response => {
        this.message = response.data.message;
      })
      .catch(error => {
        console.error(error);
      });
  }
};
</script>

<template>
  <div>
    <h1>{{ message }}</h1>
  </div>
</template>